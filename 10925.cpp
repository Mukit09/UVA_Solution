#include<stdio.h>
#include<string.h>
int main()
{
	char ch1[1100],ch2[1100],ch3[1100],a[1100];
	long c,x,y,i,k,l,m,n,f,sum,z,o=1;
	while(scanf("%ld%ld",&z,&f)==2)
	{
		if(z==0&&f==0)
			break;
		getchar();
		gets(ch1);
		z--;
		for(i=0;i<z;i++)
		{
			gets(ch2);
			x=m=strlen(ch1)-1;
			y=n=strlen(ch2)-1;
			c=k=0;
			while(k>=0)
			{
				sum=(ch1[x--]-48)+(ch2[y--]-48)+c;
				if(sum<10)
				{
					ch3[k++]=sum+48;
					c=0;
				}
				if(sum>=10)
				{
					ch3[k++]=(sum%10)+48;
					c=sum/10;
				}
				m--;
				n--;
				if(m<0&&n<0)
				{
					if(c>0)
						ch3[k++]=c+48;
					break;
				}
				if(x<0)
					ch1[++x]='0';
				if(y<0)
					ch2[++y]='0';
			}
			c=y=0;
			ch3[k]=0;
			for(x=k-1;x>=0;x--)
			{
				if(c==0&&ch3[x]=='0'&&x!=0)
					continue;
				else
				{
					ch1[y++]=ch3[x];
					c=1;
				}
				ch1[y]=0;
			}
		}
	    l=strlen(ch1);
		k=c=0;
		for(i=0;i<l;i++)
		{
			if(ch1[i]=='0'&&c==0&&i!=0)
				continue;
			else
			{
				ch2[k++]=ch1[i];
				c=1;
			}
			ch2[k]=0;
		}
		l=strlen(ch2);
		k=c=0;
		for(i=0;i<l;i++)
		{
			c=(10*c)+(ch2[i]-48);
			a[k++]=(c/f)+48;
			c%=f;
		}
		a[k]=0;
		if(k==0)
		{
			a[0]='0';
			a[1]=0;
		}
		c=0;
		printf("Bill #%ld costs %s: each friend should pay ",o++,ch2);
		for(i=0;i<k;i++)
		{
			if(c==0&&a[i]=='0'&&i!=k-1)
				continue;
			else
			{
				c=1;
				putchar(a[i]);
			}
		}
		printf("\n\n");
	}
	return 0;
}
